#!/bin/bash

# Module Temp Sensor Setting.
# Step 1: for this setting, it should write to the data register with alert threshold value 85C (0x55)
# Step 2: send the nsmtool raw command to FPGA to set the thremal threshold
# nsmtool raw -v -m 12 -d 0x10 0xde 0x80 0x89 0x03 0x01 0x02 {sensor id} ${limit}

nsmtool raw -m 12 -d 0x10 0xde 0x80 0x89 0x03 0x01 0x02 0x88 0x55
nsmtool raw -m 12 -d 0x10 0xde 0x80 0x89 0x03 0x01 0x02 0x89 0x55
nsmtool raw -m 12 -d 0x10 0xde 0x80 0x89 0x03 0x01 0x02 0x8a 0x55
nsmtool raw -m 12 -d 0x10 0xde 0x80 0x89 0x03 0x01 0x02 0x8b 0x55
